export class ForumPostRequest
{
        /// <summary>
        /// Email.
        /// </summary>
        public Email:string;

        /// <summary>
        /// User name on forum.
        /// </summary>
        public UserName:string;

        /// <summary>
        /// Original url.
        /// </summary>
        public OriginalUrl:string;

        /// <summary>
        /// Category id.
        /// </summary>
        public CategoryId:number;

        /// <summary>
        /// Title.
        /// </summary>
        public Title:string;

        /// <summary>
        /// Message.
        /// </summary>
        public Message:string;

        /// <summary>
        /// Exception message.
        /// </summary>
        public ExceptionMessage:string;

        /// <summary>
        /// Inner exception message.
        /// </summary>
        public InnerExceptionMessage:string;

        /// <summary>
        /// Folder path.
        /// </summary>
        public FolderPath:string;

        /// <summary>
        /// Is private post flag.
        /// </summary>
        public IsPrivatePost:boolean;

        /// <summary>
        /// Send notification.
        /// </summary>
        public IsSendNotification:boolean;


}